package id.ac.polban.jtk.mufidjamaluddin.RoosterApp.Model;

/**
 *
 * @author mufidjamaluddin
 */
public class JadwalDosen 
{
    private String tanggal;
    private String jam;
    private String nama_mk;
    private String nama_kelas;
    private String nama_ruangan;

    /**
     * @return the tanggal
     */
    public String getTanggal() {
        return tanggal;
    }

    /**
     * @param tanggal the tanggal to set
     */
    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    /**
     * @return the jam
     */
    public String getJam() {
        return jam;
    }

    /**
     * @param jam the jam to set
     */
    public void setJam(String jam) {
        this.jam = jam;
    }

    /**
     * @return the nama_mk
     */
    public String getNama_mk() {
        return nama_mk;
    }

    /**
     * @param nama_mk the nama_mk to set
     */
    public void setNama_mk(String nama_mk) {
        this.nama_mk = nama_mk;
    }

    /**
     * @return the nama_kelas
     */
    public String getNama_kelas() {
        return nama_kelas;
    }

    /**
     * @param nama_kelas the nama_kelas to set
     */
    public void setNama_kelas(String nama_kelas) {
        this.nama_kelas = nama_kelas;
    }

    /**
     * @return the nama_ruangan
     */
    public String getNama_ruangan() {
        return nama_ruangan;
    }

    /**
     * @param nama_ruangan the nama_ruangan to set
     */
    public void setNama_ruangan(String nama_ruangan) {
        this.nama_ruangan = nama_ruangan;
    }
}
