    private void tblShowJadwalDsnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblShowJadwalDsnActionPerformed
        // row title
        String[] rowTitle = {"Tanggal", "Pukul", "Matakuliah", "Kelas", "Ruangan"};
        // tableView
        this.tableJdwlKlhView = TableView.getInstance(null, rowTitle);
        // set table model
        this.tableJadwalDosen.setModel(tableJdwlKlhView);
        // kelas dari inputan
        String nidk = this.inputNIDK.getText();
        // list
        List<JadwalDosen> listjdwl;
        listjdwl = CONTROLLER.getListJadwalDosen(nidk);
        // loop
        listjdwl.forEach((JadwalDosen jdwl) -> {
            String[] rowValues = {jdwl.getTanggal(), jdwl.getJam(), jdwl.getNama_mk(), jdwl.getNama_kelas(), jdwl.getNama_ruangan()};
            this.tableJdwlKlhView.addRow(rowValues);
        });
    }//GEN-LAST:event_tblShowJadwalDsnActionPerformed

